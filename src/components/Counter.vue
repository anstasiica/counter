<script>
export default {
  name: 'Counter',
  data() {
    return {
      count: 0,
      minValue: -10,
      maxValue: 12
    }
  },
  methods: {
    increment() {
      this.count++
      this.$emit('incremented', this.count)
    },
    decrement() {
      this.count--
      this.$emit('decremented', this.count)
    },
    reset() {
      this.count = 0
      this.$emit('reset', this.count)
    }
  }
}
</script>

